def main():
    print("7 + 5 = {}".format(7 + 5))
    print("13 - 9 = {}".format(13 - 9))
    print("5 / 2 = {}".format(5 / 2))
    print("73 - 8 / 2 = {}".format(73 - 8 / 2))
    print("7^6 = {}".format(7 ^ 6))  # bitwise operator
    print("9 ** 5 = {}".format(9**5))  # exponent
    print("16 ** 0.5 = {}".format(16**0.5))  # square root


if __name__ == "__main__":
    main()
